class Solution:
    @staticmethod
    def binSearchLeft(A, B):
        start, end = 1, len(A) - 1
        while end - start >= 0:
            i = (start + end) // 2
            if A[i] == B and A[i - 1] != B:
                return i
            elif A[i] >= B:
                end = i - 1
            else:
                start = i + 1
        return -1

    @staticmethod
    def binSearchRight(A, B):
        start, end = 1, len(A) - 1
        while end - start >= 0:
            i = (start + end) // 2
            if A[i] == B and A[i + 1] != B:
                return i
            elif A[i] <= B:
                start = i + 1
            else:
                end = i - 1
        return -1

    def searchRange(self, A, B):
        if not A: return [-1,-1]
        start, end = 0, len(A) - 1

        if A[0] != B:
            start = Solution.binSearchLeft(A, B)
        if A[-1] != B:
            end = Solution.binSearchRight(A, B)
        return [start, end]


