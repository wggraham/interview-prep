from sys import maxsize


class Solution:
    def threeSumClosest(self, A, B):
        A.sort()
        n = len(A)
        ans = 0
        closest = maxsize
        for i in range(n):
            l, r = i + 1, n - 1
            while l < r:
                v = A[i] + A[l] + A[r]
                if abs(B - v) < closest:
                    closest = abs(B - v)
                    ans = v
                if v < B:
                    l += 1
                else:
                    r -= 1
        return ans





